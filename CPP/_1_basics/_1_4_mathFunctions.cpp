#include <iostream>
#include <cmath>
using namespace std;
int main()
{
    cout << "Max: " << max(4, 5) << endl;
    cout << "Max: " << min(4, 5) << endl;

    cout << "Power: " << pow(2, 3) << endl;
    cout << "Sqrt: " << sqrt(9) << endl;

    cout << "Absolute: " << abs(-2) << endl;
    cout << "Round: " << round(2.123145) << endl;
    cout << "Ceiling: " << ceil(2.1414) << endl;
    cout << "Floor: " << floor(2.666) << endl;
}